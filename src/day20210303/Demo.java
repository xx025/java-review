package day20210303;

public class Demo {

    public static void main(String[] args) {
        // switch() 整数 char 枚举

        int i6 = 10;
        switch (i6) {
            case 2:
                System.out.println("i6 是2");
            case 3:
                System.out.println("i6 是3 ");
            case 1:
                System.out.println("");
            default:
                System.out.println("没找到");
        }
    }
}
